import { useState, useEffect } from 'react';
import { Link, useNavigate } from 'react-router-dom';
import { Eye, EyeOff } from 'lucide-react';
import { useGoogleLogin } from '@react-oauth/google';
import toast from 'react-hot-toast';
import { useAuthStore } from '../../../core/stores/auth.store';
import { useTransactionsStore } from '../../../core/stores/transactions.store';

const Login = () => {
  const navigate = useNavigate();
  const [email, setEmail] = useState('');
  const [password, setPassword] = useState('');
  const [showPassword, setShowPassword] = useState(false);
  const [errors, setErrors] = useState<{ email?: string; password?: string }>({});
  const { login, googleLogin, isLoading, clearError, isAuthenticated } = useAuthStore();

  useEffect(() => {
    // Redirect to dashboard if already authenticated (only when on login page)
    if (isAuthenticated) {
      navigate('/dashboard', { replace: true });
      return;
    }
    clearError();
  }, [isAuthenticated, navigate, clearError]);

  const validate = () => {
    const newErrors: { email?: string; password?: string } = {};
    if (!email.trim()) {
      newErrors.email = 'Please enter your username or email';
    } 
    // else if (!/^[\w-.]+@([\w-]+\.)+[\w-]{2,4}$/.test(email)) {
    //   newErrors.email = 'Please enter a valid email';
    // }
    if (!password) {
      newErrors.password = 'Please enter your password';
    } else if (password.length < 6) {
      newErrors.password = 'Password must be at least 6 characters';
    }
    setErrors(newErrors);
    return Object.keys(newErrors).length === 0;
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    if (!validate() || isLoading) return;
    const success = await login({ email: email.trim(), password });
    if (!success) {
      const message = useAuthStore.getState().error;
      toast.error(message || 'Login failed');
      clearError();
      return;
    }
    toast.success('Login successful', { icon: '✓', iconTheme: { primary: '#22c55e', secondary: '#fff' } });
    const { fetchUser } = useAuthStore.getState();
    await fetchUser();
    const user = useAuthStore.getState().user;
    if (user && !user.has_transaction_pin) {
      navigate('/set-transaction-pin', { replace: true });
      return;
    }
    useTransactionsStore.getState().fetchTransactions({ limit: 10 }).catch(() => {});
    setTimeout(() => navigate('/dashboard', { replace: true }), 0);
  };

  const handleGoogleLogin = useGoogleLogin({
    onSuccess: async (tokenResponse) => {
      try {
        const success = await googleLogin(tokenResponse.access_token);
        if (!success) {
          const message = useAuthStore.getState().error;
          toast.error(message || 'Google login failed');
          clearError();
          return;
        }
        toast.success('Login successful', { icon: '✓', iconTheme: { primary: '#22c55e', secondary: '#fff' } });
        const { fetchUser } = useAuthStore.getState();
        await fetchUser();
        const user = useAuthStore.getState().user;
        if (user && !user.has_transaction_pin) {
          navigate('/set-transaction-pin', { replace: true });
          return;
        }
        useTransactionsStore.getState().fetchTransactions({ limit: 10 }).catch(() => {});
        setTimeout(() => navigate('/dashboard', { replace: true }), 0);
      } catch (error) {
        toast.error('Google login failed. Please try again.');
      }
    },
    onError: () => {
      toast.error('Google login failed. Please try again.');
    },
  });

  return (
    <div className="min-h-screen bg-[var(--bg-primary)] flex flex-col">
      <div className="flex-1 flex flex-col justify-center px-5 sm:px-6 py-8 sm:py-12 max-w-md mx-auto w-full">
        {/* Logo */}
        <div className="flex justify-center mb-8 sm:mb-12">
          <Link to="/">
            <div className="dark:bg-white dark:rounded-lg dark:p-2 inline-block">
              <img src="/img/logo2.png" alt="Terrabyte" className="h-8 sm:h-9 object-contain" />
            </div>
          </Link>
        </div>

        <h1 className="text-xl sm:text-2xl font-semibold text-[var(--text-primary)] text-center">
          Welcome Back!
        </h1>
        <p className="text-sm sm:text-base text-[var(--text-secondary)] text-center mt-2 mb-6">
          Login to continue your journey.
        </p>

        <form onSubmit={handleSubmit} className="space-y-5">
          <div>
            <label htmlFor="email" className="block text-sm font-medium text-[var(--text-primary)] mb-1.5">
              Username / Email Address
            </label>
            <input
              id="email"
              type="text"
              value={email}
              onChange={(e) => {
                setEmail(e.target.value);
                if (errors.email) setErrors((prev) => ({ ...prev, email: undefined }));
              }}
              placeholder="Enter your email"
              className="w-full px-4 py-3 rounded-xl border border-[var(--border-color)] bg-[var(--bg-input)] text-[var(--text-primary)] placeholder-[var(--text-muted)] focus:outline-none focus:ring-2 focus:ring-[var(--brand-primary)] focus:border-transparent transition-colors"
              autoComplete="email"
            />
            {errors.email && (
              <p className="mt-1 text-sm text-[var(--error)]">{errors.email}</p>
            )}
          </div>

          <div>
            <label htmlFor="password" className="block text-sm font-medium text-[var(--text-primary)] mb-1.5">
              Password
            </label>
            <div className="relative">
              <input
                id="password"
                type={showPassword ? 'text' : 'password'}
                value={password}
                onChange={(e) => {
                  setPassword(e.target.value);
                  if (errors.password) setErrors((prev) => ({ ...prev, password: undefined }));
                }}
                placeholder="Enter your password"
                className="w-full px-4 py-3 pr-12 rounded-xl border border-[var(--border-color)] bg-[var(--bg-input)] text-[var(--text-primary)] placeholder-[var(--text-muted)] focus:outline-none focus:ring-2 focus:ring-[var(--brand-primary)] focus:border-transparent transition-colors"
                autoComplete="current-password"
              />
              <button
                type="button"
                onClick={() => setShowPassword(!showPassword)}
                className="absolute right-3 top-1/2 -translate-y-1/2 p-1 text-[var(--text-muted)] hover:text-[var(--text-primary)] transition-colors"
                aria-label={showPassword ? 'Hide password' : 'Show password'}
              >
                {showPassword ? <EyeOff size={20} /> : <Eye size={20} />}
              </button>
            </div>
            {errors.password && (
              <p className="mt-1 text-sm text-[var(--error)]">{errors.password}</p>
            )}
          </div>

          <div className="flex justify-end">
            <Link
              to="/forgot-password"
              className="text-sm font-semibold text-brand-primary hover:text-brand-primary-dark transition-colors"
            >
              Forgot Password?
            </Link>
          </div>

          <button
            type="submit"
            disabled={isLoading}
            className="w-full py-3 rounded-full bg-brand-primary text-white font-semibold hover:bg-brand-primary-dark disabled:opacity-70 disabled:cursor-not-allowed transition-all duration-300"
          >
            {isLoading ? 'Logging in...' : 'Login'}
          </button>
        </form>

        <div className="relative my-6">
          <div className="absolute inset-0 flex items-center">
            <div className="w-full border-t border-[var(--border-color)]"></div>
          </div>
          <div className="relative flex justify-center text-sm">
            <span className="px-2 bg-[var(--bg-primary)] text-[var(--text-muted)]">Or </span>
          </div>
        </div>

        <button
          type="button"
          onClick={() => handleGoogleLogin()}
          disabled={isLoading}
          className="w-full py-3 rounded-full border-2 border-[var(--border-color)] bg-[var(--bg-input)] text-[var(--text-primary)] font-semibold hover:bg-[var(--bg-hover)] disabled:opacity-70 disabled:cursor-not-allowed transition-all duration-300 flex items-center justify-center gap-2"
        >
          <svg className="w-5 h-5" viewBox="0 0 24 24">
            <path
              fill="currentColor"
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
            />
            <path
              fill="currentColor"
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
            />
            <path
              fill="currentColor"
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
            />
            <path
              fill="currentColor"
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
            />
          </svg>
          Continue with Google
        </button>

        <p className="text-center text-sm text-[var(--text-secondary)] mt-6">
          Don&apos;t have an account?{' '}
          <Link to="/signup" className="font-semibold text-brand-primary hover:text-brand-primary-dark transition-colors">
            Sign Up
          </Link>
        </p>
        <p className="text-center text-xs text-[var(--text-muted)] mt-4">
          <Link to="/" className="hover:text-brand-primary transition-colors">
            ← Back to home
          </Link>
        </p>
      </div>
    </div>
  );
};

export default Login;
